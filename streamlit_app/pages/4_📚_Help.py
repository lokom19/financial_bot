# FAQ
st.markdown("## ❓ Частые вопросы")

with st.expander("Как часто обновляются прогнозы?"):
    st.markdown("""
    Прогнозы обновляются при запуске команды `make train`.
    Рекомендуется запускать обучение:
    - Ежедневно после закрытия биржи (после 19:00 МСК)
    - Или настроить автоматический запуск через cron
    """)

with st.expander("Почему разные модели дают разные сигналы?"):
    st.markdown("""
    Каждая модель имеет свои особенности:
    - Линейные модели (Ridge) ищут линейные зависимости
    - Деревья (XGBoost, LightGBM) находят нелинейные паттерны
    - ARIMA фокусируется на трендах и сезонности

    Разногласия между моделями - это нормально. Консенсус помогает
    выделить наиболее надёжные сигналы.
    """)

with st.expander("Что делать, если нет сигналов?"):
    st.markdown("""
    Если сигналов нет:
    1. Проверьте, что данные загружены: `make data-status`
    2. Запустите обучение: `make train`
    3. Снизьте минимальный порог консенсуса

    Отсутствие сигналов может также означать, что рынок неопределён
    и модели не видят явного направления.
    """)

