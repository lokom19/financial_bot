# Metrics explanation
st.markdown("## 📊 Объяснение метрик")

col1, col2 = st.columns(2)

with col1:
    st.markdown("""
    ### Метрики качества модели

    **R² (R-squared, коэффициент детерминации)**
    - Показывает, насколько хорошо модель объясняет вариацию цен
    - Диапазон: 0 - 1 (чем выше, тем лучше)
    - R² > 0.7 считается хорошим результатом

    **RMSE (Root Mean Square Error)**
    - Среднеквадратичная ошибка прогноза
    - Измеряется в тех же единицах, что и цена
    - Чем меньше, тем лучше

    **MAE (Mean Absolute Error)**
    - Средняя абсолютная ошибка
    - Показывает среднее отклонение прогноза от реальной цены
    - Чем меньше, тем лучше

    **MAPE (Mean Absolute Percentage Error)**
    - Средняя ошибка в процентах
    - MAPE < 10% считается хорошим результатом
    """)

with col2:
    st.markdown("""
    ### Метрики торговой стратегии

    **Direction Accuracy (Точность направления)**
    - Процент правильно предсказанных направлений
    - > 55% лучше случайного угадывания
    - > 60% считается хорошим результатом

    **Win Rate (Доля прибыльных сделок)**
    - Процент сделок с положительным результатом
    - > 50% в сочетании с хорошим Profit Factor

    **Profit Factor (Коэффициент прибыли)**
    - Отношение суммы прибылей к сумме убытков
    - > 1.0 = стратегия прибыльна
    - > 1.5 = хорошая стратегия
    - > 2.0 = отличная стратегия

    **Cumulative Return (Накопленная доходность)**
    - Общая доходность стратегии на тестовых данных
    - Положительное значение = стратегия заработала
    """)

st.markdown("---")

# Consensus explanation
st.markdown("## 🤝 Консенсус моделей")

st.markdown("""
**Консенсус** - это процент моделей, которые дают одинаковый сигнал для инструмента.

| Консенсус | Интерпретация | Уверенность |
|-----------|---------------|-------------|
| 50-60% | Слабый консенсус | Низкая |
| 60-75% | Умеренный консенсус | Средняя |
| 75-90% | Сильный консенсус | Высокая |
| > 90% | Очень сильный консенсус | Очень высокая |

### Пример:
Если 5 из 7 моделей (71%) дают сигнал BUY для SBER, это означает:
- Большинство алгоритмов согласны, что акция вырастет
- Консенсус 71% - умеренно сильный сигнал
- Стоит рассмотреть покупку, но с осторожностью
""")

st.markdown("---")

